const MORSE_TABLE = {
    '.-':     'a',
    '-...':   'b',
    '-.-.':   'c',
    '-..':    'd',
    '.':      'e',
    '..-.':   'f',
    '--.':    'g',
    '....':   'h',
    '..':     'i',
    '.---':   'j',
    '-.-':    'k',
    '.-..':   'l',
    '--':     'm',
    '-.':     'n',
    '---':    'o',
    '.--.':   'p',
    '--.-':   'q',
    '.-.':    'r',
    '...':    's',
    '-':      't',
    '..-':    'u',
    '...-':   'v',
    '.--':    'w',
    '-..-':   'x',
    '-.--':   'y',
    '--..':   'z',
    '.----':  '1',
    '..---':  '2',
    '...--':  '3',
    '....-':  '4',
    '.....':  '5',
    '-....':  '6',
    '--...':  '7',
    '---..':  '8',
    '----.':  '9',
    '-----':  '0',
};

function decode(expr) {
    let symbol = expr.split('**********');
    let result = '';

    for (let key of symbol) {
        let prevSymbol = key.match(/\d{10}/g).reduce((prevSymbol, element) => {

            prevSymbol += MORSE_TABLE[element.replace(/00/g, '').replace(/10/g, '.').replace(/11/g, '-')];

            return prevSymbol;
        }, ' ');

        result += prevSymbol;
    }
    return result.trim();
}

module.exports = {
    decode
}